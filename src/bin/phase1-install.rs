use std::env;
use std::fs;
use std::path::{Path, PathBuf};

const CURRENT_VERSION: &str = env!("CARGO_PKG_VERSION");
const STABLE_VERSION: &str = "4.0.0";
const PREVIOUS_STABLE_VERSION: &str = "3.10.9";
const DEFAULT_INSTALL_ROOT: &str = "phase1.local";

fn main() {
    let args = env::args().skip(1).collect::<Vec<_>>();
    match run(&args) {
        Ok(output) => {
            print!("{output}");
            std::process::exit(0);
        }
        Err(err) => {
            eprintln!("phase1-install: {err}");
            std::process::exit(1);
        }
    }
}

fn run(args: &[String]) -> Result<String, String> {
    match args.first().map(String::as_str).unwrap_or("plan") {
        "plan" | "status" | "doctor" => Ok(plan()),
        "paths" | "path" => Ok(paths_report()),
        "write-launcher" | "launcher" | "install" => write_launcher(),
        "help" | "-h" | "--help" => Ok(help()),
        other => Err(format!("unknown install action '{other}'\n\n{}", help())),
    }
}

fn help() -> String {
    "phase1-install // guarded boot-install helper\n\nusage:\n  phase1-install plan\n  phase1-install paths\n  phase1-install write-launcher\n\nboot use:\n  From the Phase1 boot selector, open the storage/Git/Rust dock, then use this helper to inspect or install a local launcher.\n\nsafety:\n  plan and paths are read-only.\n  write-launcher requires PHASE1_SAFE_MODE=0 and PHASE1_ALLOW_HOST_TOOLS=1.\n\nversion identity:\n  current edge, stable, and previous stable are reported together.\n"
        .to_string()
}

fn plan() -> String {
    let root = install_root();
    let launcher = launcher_path(&root);
    format!(
        "phase1 install plan\nmode            : guarded local launcher\ncurrent edge    : v{CURRENT_VERSION}\nstable          : v{STABLE_VERSION}\nprevious stable : v{PREVIOUS_STABLE_VERSION}\ninstall root    : {}\nlauncher        : {}\nrepo root       : {}\nhost tools      : {}\nwrite command   : phase1-install write-launcher\nboot flow       : boot selector -> storage/Git/Rust dock -> install plan/write-launcher\nsafety          : no system directories are modified; launcher writes only under the install root\n",
        root.display(),
        launcher.display(),
        repo_root().display(),
        if host_tools_allowed() { "enabled" } else { "guarded" },
    )
}

fn paths_report() -> String {
    let root = install_root();
    format!(
        "phase1 install paths\nroot     : {}\nbin      : {}\nlauncher : {}\nrepo     : {}\n",
        root.display(),
        root.join("bin").display(),
        launcher_path(&root).display(),
        repo_root().display()
    )
}

fn write_launcher() -> Result<String, String> {
    require_host_tools("write-launcher")?;
    let root = install_root();
    let bin = root.join("bin");
    let launcher = launcher_path(&root);
    let repo = repo_root();
    fs::create_dir_all(&bin).map_err(|err| err.to_string())?;

    let script = format!(
        "#!/usr/bin/env sh\nset -eu\n# Generated by phase1-install for Phase1 v{CURRENT_VERSION}.\n# Stable v{STABLE_VERSION}; previous stable v{PREVIOUS_STABLE_VERSION}.\ncd '{}'\nexec cargo run --bin phase1 -- \"$@\"\n",
        shell_escape_path(&repo)
    );
    fs::write(&launcher, script).map_err(|err| err.to_string())?;

    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        let mut perms = fs::metadata(&launcher)
            .map_err(|err| err.to_string())?
            .permissions();
        perms.set_mode(0o755);
        fs::set_permissions(&launcher, perms).map_err(|err| err.to_string())?;
    }

    Ok(format!(
        "phase1 install complete\nlauncher : {}\nrepo     : {}\nversion  : v{CURRENT_VERSION}\nnext     : {}\n",
        launcher.display(),
        repo.display(),
        shell_command(&launcher)
    ))
}

fn install_root() -> PathBuf {
    env::var_os("PHASE1_INSTALL_ROOT")
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from(DEFAULT_INSTALL_ROOT))
}

fn repo_root() -> PathBuf {
    env::var_os("PHASE1_REPO_ROOT")
        .map(PathBuf::from)
        .or_else(|| env::current_dir().ok())
        .unwrap_or_else(|| PathBuf::from("."))
}

fn launcher_path(root: &Path) -> PathBuf {
    let name = if cfg!(windows) {
        "phase1-edge.cmd"
    } else {
        "phase1-edge"
    };
    root.join("bin").join(name)
}

fn shell_command(path: &Path) -> String {
    if path.is_absolute() {
        path.display().to_string()
    } else {
        format!("./{}", path.display())
    }
}

fn shell_escape_path(path: &Path) -> String {
    path.display().to_string().replace('\'', "'\\''")
}

fn require_host_tools(command: &str) -> Result<(), String> {
    if host_tools_allowed() {
        Ok(())
    } else if safe_mode_enabled() {
        Err(format!(
            "{command}: disabled by safe boot profile; turn SHIELD off and enable TRUST HOST before writing install files"
        ))
    } else {
        Err(format!(
            "{command}: disabled; set PHASE1_ALLOW_HOST_TOOLS=1 before writing install files"
        ))
    }
}

fn safe_mode_enabled() -> bool {
    !matches!(
        env::var("PHASE1_SAFE_MODE").ok().as_deref(),
        Some("0" | "false" | "off" | "no")
    )
}

fn host_tools_allowed() -> bool {
    !safe_mode_enabled() && env::var("PHASE1_ALLOW_HOST_TOOLS").ok().as_deref() == Some("1")
}

#[cfg(test)]
mod tests {
    use super::{plan, run, CURRENT_VERSION, PREVIOUS_STABLE_VERSION, STABLE_VERSION};

    #[test]
    fn plan_reports_all_three_release_numbers() {
        let out = plan();
        assert!(out.contains(CURRENT_VERSION));
        assert!(out.contains(STABLE_VERSION));
        assert!(out.contains(PREVIOUS_STABLE_VERSION));
        assert!(out.contains("boot selector"));
        assert!(out.contains("write-launcher"));
        assert!(out.contains("repo root"));
    }

    #[test]
    fn write_launcher_is_guarded_by_default() {
        std::env::remove_var("PHASE1_SAFE_MODE");
        std::env::remove_var("PHASE1_ALLOW_HOST_TOOLS");
        let err = run(&["write-launcher".to_string()]).expect_err("write should be guarded");
        assert!(err.contains("disabled by safe boot profile"));
    }
}
